def _column_pool(col_idx: int, table_cols: int, specialisation: str) -> List[str]:
    """
    Return the grapheme pool for a given column index.

    NONE / UNIFORM / BLANK_GRADIENT
        All columns draw from the full pool — no positional separation.
        NONE/UNIFORM: blank_prob is uniform across columns (set in build_table).
        BLANK_GRADIENT: blank_prob increases left-to-right (set in build_table).
        Use table_rows >= 256 to avoid sampling artefacts.

    SPLIT  [*** CIRCULAR — kept for comparison only ***]
        Left half uses PREFIX_GRAPHEMES, right half SUFFIX_GRAPHEMES.
        Bakes in the very structure being tested.

    GRADIENT  [also partially circular]
        Columns blend from prefix-dominant to suffix-dominant.
    """
    if specialisation in ("NONE", "UNIFORM", "BLANK_GRADIENT"):
        return list(ALL_GRAPHEMES)

    frac = col_idx / max(table_cols - 1, 1)

    if specialisation == "SPLIT":
        return list(PREFIX_GRAPHEMES) if frac < 0.5 else list(SUFFIX_GRAPHEMES)

    if specialisation == "GRADIENT":
        n_pref = max(1, round((1 - frac) * len(PREFIX_GRAPHEMES)))
        n_suf  = max(1, round(frac       * len(SUFFIX_GRAPHEMES)))
        pool   = PREFIX_GRAPHEMES[:n_pref] + SUFFIX_GRAPHEMES[:n_suf]
        return list(set(pool)) if pool else list(ALL_GRAPHEMES)

    raise ValueError(f"Unknown specialisation: {specialisation}")


def build_table(table_rows: int, table_cols: int,
                specialisation: str = "SPLIT",
                blank_prob: float = 0.2,
                col_skew: float = 1.2,
                rng: Optional[random.Random] = None) -> List[List[Optional[str]]]:
    """
    Build a (table_rows x table_cols) grille table.

    For BLANK_GRADIENT specialisation, blank_prob is the *maximum* blank
    probability (applied to the rightmost column); the leftmost column has
    blank_prob=0.  This tests whether density variation alone — without any
    pool separation — can induce positional grapheme structure.

    For all other specialisations, blank_prob is uniform across columns.
    """
    if rng is None:
        rng = random.Random()

    table = []
    for _row in range(table_rows):
        row = []
        for col in range(table_cols):
            # Column-specific blank probability
            if specialisation == "BLANK_GRADIENT":
                frac = col / max(table_cols - 1, 1)
                col_blank = blank_prob * frac   # 0 at left, blank_prob at right
            else:
                col_blank = blank_prob

            if rng.random() < col_blank:
                row.append(None)
            else:
                pool = _column_pool(col, table_cols, specialisation)
                if not pool:
                    row.append(None)
                    continue
                if col_skew < 0.01:
                    g = rng.choice(pool)
                else:
                    weights = ev.make_zipf_weights(len(pool), col_skew)
                    g = rng.choices(pool, weights=weights, k=1)[0]
                row.append(g)
        table.append(row)
    return table


def build_table_from_corpus(sentences, tokenizer,
                             table_cols: int = 10,
                             table_rows: int = 256,
                             col_skew: float = 0.0,
                             rng: Optional[random.Random] = None
                             ) -> List[List[Optional[str]]]:
    """
    Build a grille table whose COLUMN DISTRIBUTIONS are learned from a real
    corpus rather than from pre-separated prefix/suffix pools.

    Method:
      1. For each word in the corpus, tokenize it and record which grapheme
         appears at each word-position index (0=first, 1=second, ...).
      2. This gives an empirical distribution P(grapheme | position).
      3. Map word-position indices to table columns: position i -> column i
         (capped at table_cols - 1).
      4. Fill each table cell by sampling from the column's learned distribution.

    If col_skew > 0, the sampling is further concentrated by Zipfian reweighting
    of the empirical distribution.  col_skew=0 (default) uses the raw empirical
    frequencies.

    This is the HONEST version of pool-based specialisation: the positional
    structure in the table reflects whatever positional structure exists in the
    source corpus — no structure is imposed by construction.

    Source corpora used:
      G8: English (Moby Dick)   — natural language with real positional structure
      G9: VMS Markov baseline   — slot-based synthetic text (circular but explicit)
      G10: Random EVA sequences  — structureless baseline; expected E->S ~0
    """
    if rng is None:
        rng = random.Random()

    # Build positional frequency counts
    pos_counts: List[Counter] = [Counter() for _ in range(table_cols)]
    for sent in sentences:
        for w in sent:
            toks = tokenizer(w)
            for i, g in enumerate(toks[:table_cols]):
                pos_counts[i][g] += 1

    # Convert to weighted pool per column
    col_pools: List[List[str]] = []
    col_weights: List[List[float]] = []
    for col in range(table_cols):
        cnt = pos_counts[col]
        if not cnt:
            # No data for this position: fall back to uniform full pool
            col_pools.append(list(ALL_GRAPHEMES))
            col_weights.append(ev.make_flat_weights(len(ALL_GRAPHEMES)))
        else:
            graphemes = list(cnt.keys())
            freqs     = [cnt[g] for g in graphemes]
            if col_skew < 0.01:
                total = sum(freqs)
                weights = [f / total for f in freqs]
            else:
                # Reweight by rank with Zipfian exponent
                ranked   = sorted(range(len(freqs)), key=lambda i: -freqs[i])
                zipf_w   = ev.make_zipf_weights(len(freqs), col_skew)
                weights  = [0.0] * len(freqs)
                for rank, idx in enumerate(ranked):
                    weights[idx] = zipf_w[rank]
            col_pools.append(graphemes)
            col_weights.append(weights)

    # Fill table by sampling from learned column distributions
    table = []
    for _ in range(table_rows):
        row = []
        for col in range(table_cols):
            g = rng.choices(col_pools[col], weights=col_weights[col], k=1)[0]
            row.append(g)
        table.append(row)
    return table


