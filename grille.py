class GrilleConfig:
    """
    Mirrors the GeneratorConfig interface from signatures_v26 so the same
    run_config / aggregate / print functions can be used unchanged.
    """

    def __init__(self, label: str,
                 table_rows: int = 16,
                 table_cols: int = 10,
                 n_holes: int = 4,
                 specialisation: str = "SPLIT",
                 blank_prob: float = 0.2,
                 col_skew: float = 1.2,
                 mode: str = "RANDOM",
                 boundary_pairs=None,
                 pair_strength: float = 3.0,
                 n_words: int = 37000,
                 source_corpus=None,
                 source_tokenizer=None,
                 **kwargs):
        self.label            = label
        self.table_rows       = table_rows
        self.table_cols       = table_cols
        self.n_holes          = n_holes
        self.specialisation   = specialisation
        self.blank_prob       = blank_prob
        self.col_skew         = col_skew
        self.mode             = mode
        self.boundary_pairs   = boundary_pairs
        self.pair_strength    = pair_strength
        self.n_words          = n_words
        self.source_corpus    = source_corpus     # pre-built sentences for learned table
        self.source_tokenizer = source_tokenizer  # tokenizer for source corpus
        self.tokenizer        = TOKENIZER
        self.n_graphemes      = len(ALL_GRAPHEMES)
        self.vocab_size       = table_rows  # for display
        # Row-sequential parameters (used when mode == "SEQUENTIAL")
        self.p_jump           = kwargs.get("p_jump", 0.0)
        self.source_words     = kwargs.get("source_words", None)

    def generate(self, seed: int = 42) -> List[List[str]]:
        rng = random.Random(seed)
        if self.source_corpus is not None:
            # Honest table: column distributions learned from real corpus
            src_tok = self.source_tokenizer or TOKENIZER
            table = build_table_from_corpus(
                self.source_corpus, src_tok,
                table_cols=self.table_cols,
                table_rows=self.table_rows,
                col_skew=self.col_skew,
                rng=rng)
        else:
            table = build_table(self.table_rows, self.table_cols,
                                self.specialisation, self.blank_prob,
                                self.col_skew, rng)

        if self.mode == "SEQUENTIAL":
            # Row-sequential: uses source_words directly, not a table
            if self.source_words is None:
                raise ValueError(
                    "SEQUENTIAL mode requires source_words. "
                    "Pass source_words=load_english_corpus(path) or "
                    "source_words=random_word_list to the config.")
            words = generate_corpus_sequential(
                self.source_words, self.n_holes, self.n_words,
                self.p_jump, rng)
        elif self.mode == "RANDOM":
            words = generate_corpus_random(
                table, self.table_cols, self.n_holes, self.n_words, rng)
        elif self.mode == "SHIFT":
            words = generate_corpus_shift(
                table, self.table_cols, self.n_holes, self.n_words, rng)
        elif self.mode == "ROTATE":
            words = generate_corpus_rotate(
                table, self.table_cols, self.n_holes, self.n_words, rng)
        elif self.mode == "WEIGHTED":
            words = generate_corpus_weighted_rows(
                table, self.table_cols, self.n_holes, self.n_words, rng,
                self.boundary_pairs, self.pair_strength)
        else:
            raise ValueError(f"Unknown mode: {self.mode}")

        return words_to_sentences(words, rng=rng)


