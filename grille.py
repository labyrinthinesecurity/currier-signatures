def run_main(n_runs, base_seed, n_words, n_shuffles, english_path=None):
    # --- Pool-separated configs (G0-G5) ---
    cfgs = grille_configs(n_words)

    # --- Honest table configs (G8-G10) ---
    print(f"  Building source corpora for honest table configs (G8/G9/G10)...",
          end="", flush=True)
    corpora = build_honest_source_corpora()
    print(f"  done.")
    honest  = grille_honest_configs(n_words, corpora)

    # --- Row-sequential configs (G_seq) ---
    print(f"  Building source words for row-sequential configs...",
          end="", flush=True)
    seq_sources = build_sequential_source_words(english_path, n_words)
    print(f"  done.")
    sequential  = grille_sequential_configs(n_words, seq_sources)

    all_agg = []
    for cfg in cfgs + honest + sequential:
        table_info = (f"table={cfg.table_rows}x{cfg.table_cols}"
                      if cfg.mode != "SEQUENTIAL"
                      else f"source={len(cfg.source_words or [])} words")
        print(f"  Running: {cfg.label}  "
              f"({n_runs} runs, {cfg.n_graphemes} graphemes, {table_info})...",
              end="", flush=True)
        results = run_grille_config(cfg, n_runs, base_seed, n_shuffles)
        agg     = ev.aggregate(results)
        all_agg.append(agg)
        _, nm = ev.joint_profile_match(agg)
        print(f"  E->S={agg['es_pct_mean']:.1f}%  "
              f"MI={agg['mi_orig_mean']:.4f}  "
              f"Shape={agg['shape']}  {nm}/4")

    n_sep  = len(cfgs)
    n_hon  = len(honest)
    sep_agg   = all_agg[:n_sep]
    hon_agg   = all_agg[n_sep:n_sep + n_hon]
    seq_agg   = all_agg[n_sep + n_hon:]

    print_grille_table(sep_agg,
        "GRILLE RESULTS — POOL-SEPARATED (G0-G5, circular on Sig1)")
    print_grille_table(hon_agg,
        "GRILLE RESULTS — HONEST TABLE (G8-G10, no pre-imposed pool separation)")
    if seq_agg:
        print_grille_table(seq_agg,
            "GRILLE RESULTS — ROW-SEQUENTIAL (G_seq, honest MI test)")
        print(f"  Key: MI in G_seq_English is bounded above by English source MI.")
        print(f"  Net sequential signal = MI_orig - MI_shuf (see sensitivity sweep).")
        print(f"  G_seq_Random is the null: net signal should be ~0 regardless of p_jump.")
    print_grille_interpretation(all_agg)
    return all_agg


def run_falsification(n_runs, base_seed, n_words, n_shuffles):
    cfgs    = grille_nearmiss_configs(n_words)
    all_agg = []
    for cfg in cfgs:
        print(f"  Running: {cfg.label}  ({n_runs} runs)...",
              end="", flush=True)
        results = run_grille_config(cfg, n_runs, base_seed, n_shuffles)
        agg     = ev.aggregate(results)
        all_agg.append(agg)
        _, nm = ev.joint_profile_match(agg)
        print(f"  E->S={agg['es_pct_mean']:.1f}%  "
              f"MI={agg['mi_orig_mean']:.4f}  "
              f"Shape={agg['shape']}  {nm}/4")

    print_grille_table(all_agg, "GRILLE NEAR-MISS FALSIFICATION")
    print_grille_interpretation(all_agg)
    return all_agg


def run_sensitivity(n_runs, base_seed, n_words, n_shuffles):
    sweeps = [
        ("Blank Probability",    sens_blank_prob(n_words)),
        ("Number of Holes",      sens_n_holes(n_words)),
        ("Table Columns",        sens_table_cols(n_words)),
        ("Column Skew (alpha)",  sens_col_skew(n_words)),
        ("Table Rows",           sens_table_rows(n_words)),
    ]
    for title, cfgs in sweeps:
        print(f"\n  --- {title} ---")
        agg_list = []
        for cfg in cfgs:
            print(f"  Running: {cfg.label}  ({n_runs} runs)...",
                  end="", flush=True)
            agg = ev.aggregate(
                run_grille_config(cfg, n_runs, base_seed, n_shuffles))
            agg_list.append(agg)
            _, nm = ev.joint_profile_match(agg)
            print(f"  E->S={agg['es_pct_mean']:.1f}%  "
                  f"MI={agg['mi_orig_mean']:.4f}  {nm}/4")
        print_grille_sens_table(agg_list, title)


