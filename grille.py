def run_grille_config(cfg: GrilleConfig, n_runs: int = 20,
                      base_seed: int = 42, n_shuffles: int = 3):
    results = []
    for i in range(n_runs):
        try:
            sents = cfg.generate(seed=base_seed + i * 137)
        except Exception as exc:
            print(f"\n  ERROR {cfg.label} run {i}: {exc}")
            import traceback; traceback.print_exc()
            continue
        results.append(
            ev.evaluate_corpus_fast(sents, cfg.tokenizer, cfg.label, n_shuffles))
    return results


