def generate_corpus_random(table, table_cols, n_holes, n_words, rng):
    """
    Rugg's core model: each word uses a freshly sampled random grille position.
    Words are generated independently — no sequential structure.
    """
    words = []
    attempts = 0
    while len(words) < n_words and attempts < n_words * 20:
        attempts += 1
        holes = random_grille(table_cols, n_holes, rng)
        w = apply_grille(table, holes, rng)
        if len(w) >= 2:
            words.append(w)
    return words


def generate_corpus_shift(table, table_cols, n_holes, n_words, rng):
    """
    Shifted grille: the grille moves right by 1 column after each word.
    Creates positional correlation between consecutive words (some MI expected).
    """
    holes = random_grille(table_cols, n_holes, rng)
    words = []
    attempts = 0
    while len(words) < n_words and attempts < n_words * 20:
        attempts += 1
        w = apply_grille(table, holes, rng)
        if len(w) >= 2:
            words.append(w)
        holes = shift_grille(holes, table_cols)
    return words


def generate_corpus_rotate(table, table_cols, n_holes, n_words, rng):
    """
    Rotating grille: cycles through all unique rotations of a fixed grille mask.
    Closer to Rugg's original rotating card mechanism.
    """
    rotations = rotate_grilles(table_cols, n_holes)
    if not rotations:
        return generate_corpus_random(table, table_cols, n_holes, n_words, rng)
    words = []
    attempts = 0
    rot_idx = 0
    while len(words) < n_words and attempts < n_words * 20:
        attempts += 1
        holes = rotations[rot_idx % len(rotations)]
        w = apply_grille(table, holes, rng)
        if len(w) >= 2:
            words.append(w)
        rot_idx += 1
    return words


def generate_corpus_weighted_rows(table, table_cols, n_holes, n_words, rng,
                                  boundary_pairs=None, pair_strength=3.0):
    """
    Near-miss variant G6: boundary-pair-weighted row selection.
    After generating a word, the next word's row is biased toward rows whose
    leftmost non-blank cell matches a preferred (prev_end, next_start) pair.
    This injects a small amount of MI without full Markov machinery.
    """
    # Pre-build: for each (end_grapheme, start_grapheme) pair in boundary_pairs,
    # track which rows produce that start grapheme.
    pair_bonus = {}
    if boundary_pairs:
        for entry in boundary_pairs:
            eg, sg = entry[0], entry[1]
            st = entry[2] if len(entry) == 3 else pair_strength
            pair_bonus[(eg, sg)] = st

    def _get_start(row, holes):
        for p in sorted(holes):
            if p < len(row) and row[p] is not None:
                return row[p]
        return None

    def _get_end(word_str):
        toks = TOKENIZER(word_str)
        return toks[-1] if toks else None

    holes = random_grille(table_cols, n_holes, rng)
    words = []
    prev_end = None
    attempts = 0

    while len(words) < n_words and attempts < n_words * 30:
        attempts += 1
        # Weight rows by whether their start matches a preferred pair
        if prev_end and pair_bonus:
            row_weights = []
            for row in table:
                sg = _get_start(row, holes)
                bonus = pair_bonus.get((prev_end, sg), 1.0) if sg else 1.0
                row_weights.append(bonus)
            total = sum(row_weights)
            row_weights = [w / total for w in row_weights]
            chosen_row = rng.choices(table, weights=row_weights, k=1)[0]
        else:
            chosen_row = rng.choice(table)

        graphemes = [chosen_row[p] for p in holes
                     if p < len(chosen_row) and chosen_row[p] is not None]
        w = "".join(graphemes)
        if len(w) >= 2:
            words.append(w)
            prev_end = _get_end(w)
        holes = shift_grille(holes, table_cols)

    return words


def load_english_corpus(path: str) -> List[str]:
    """
    Load a plain-text English corpus and return a list of lowercase words.
    Punctuation is stripped; words shorter than 2 characters are dropped.
    Used as the source for G_seq_English configs.
    """
    import re
    words = []
    try:
        with open(path, encoding="utf-8", errors="replace") as f:
            for line in f:
                for w in line.split():
                    w = re.sub(r"[^a-zA-Z]", "", w).lower()
                    if len(w) >= 2:
                        words.append(w)
    except FileNotFoundError:
        raise FileNotFoundError(
            "English corpus not found at '" + path + "'. "
            "Set --english-path to the location of the file. "
            "Expected format: plain text, one paragraph per line."
        )
    return words


def generate_corpus_sequential(source_words: List[str],
                                n_holes: int,
                                n_words: int,
                                p_jump: float,
                                rng: Optional[random.Random] = None) -> List[str]:
    """
    Row-sequential grille traversal over a table whose rows are the characters
    of successive source words.

    Mechanism:
      - Table row i = character list of source_words[i].
      - The grille selects n_holes random character positions from the current row.
      - After each word, advance to the next row with probability (1 - p_jump);
        with probability p_jump, jump to a uniformly random row.

    Variants (controlled by p_jump):
      p_jump = 0.0   pure sequential (rows visited in order, wrapping)
      p_jump = 0.0 with occasional perturbation  (e.g. p_jump = 0.05 or 0.1)
      p_jump = 1.0   pure random (Rugg's original model, rows drawn uniformly)

    Key property: any MI in the output is BOUNDED ABOVE by the MI in the source.
    The grille cannot create MI ex nihilo — it can only transmit or attenuate
    whatever sequential structure exists in the row ordering.

    With an English source: net MI decreases monotonically from ~0.64 (p=0.0)
    to ~0 (p=1.0), giving a clean falsifiable sweep.
    With a random source: net MI ≈ 0 for all p_jump values (validated empirically).
    """
    if rng is None:
        rng = random.Random()

    n_rows = len(source_words)
    if n_rows == 0:
        return []

    row_idx = 0
    words_out: List[str] = []
    attempts = 0

    while len(words_out) < n_words and attempts < n_words * 20:
        attempts += 1
        row = list(source_words[row_idx % n_rows])
        if not row:
            row_idx = (row_idx + 1) % n_rows
            continue

        k = min(n_holes, len(row))
        holes = sorted(rng.sample(range(len(row)), k))
        w = "".join(row[p] for p in holes)
        if len(w) >= 2:
            words_out.append(w)

        # Advance row pointer
        if rng.random() < p_jump:
            row_idx = rng.randint(0, n_rows - 1)
        else:
            row_idx = (row_idx + 1) % n_rows

    return words_out


def words_to_sentences(words, min_len=4, max_len=12, rng=None):
    if rng is None:
        rng = random.Random()
    sentences, i = [], 0
    while i < len(words):
        n = rng.randint(min_len, max_len)
        s = words[i:i + n]
        if len(s) >= 3:
            sentences.append(s)
        i += n
    return sentences


