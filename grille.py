def apply_grille(table: List[List[Optional[str]]],
                 hole_positions: List[int],
                 rng: Optional[random.Random] = None) -> str:
    """
    Apply a grille (defined by its hole positions) to a randomly selected
    table row.  Concatenate non-blank graphemes at hole positions.
    Returns the resulting word (may be empty string if all cells are blank).
    """
    if rng is None:
        rng = random.Random()
    row = rng.choice(table)
    graphemes = [row[p] for p in hole_positions if p < len(row) and row[p] is not None]
    return "".join(graphemes)


def random_grille(table_cols: int, n_holes: int, rng: random.Random) -> List[int]:
    """Return a random grille: n_holes distinct column positions."""
    return sorted(rng.sample(range(table_cols), min(n_holes, table_cols)))


def shift_grille(current_holes: List[int], table_cols: int) -> List[int]:
    """Shift all hole positions right by 1, wrapping around."""
    return [(p + 1) % table_cols for p in current_holes]


def rotate_grilles(table_cols: int, n_holes: int) -> List[List[int]]:
    """
    Generate all unique rotations of a grille with holes at positions 0..n_holes-1.
    Returns a list of hole-position lists.
    """
    base = list(range(n_holes))
    rotations = []
    current = base[:]
    for _ in range(table_cols):
        rotations.append(sorted(current))
        current = [(p + 1) % table_cols for p in current]
    # Deduplicate
    seen = set()
    unique = []
    for r in rotations:
        key = tuple(r)
        if key not in seen:
            seen.add(key)
            unique.append(r)
    return unique


