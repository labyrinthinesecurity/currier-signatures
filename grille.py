def build_sequential_source_words(english_path: Optional[str] = None,
                                   n_words: int = 37000) -> Dict[str, List[str]]:
    """
    Build the word lists used as row sources for SEQUENTIAL configs.

    Returns a dict with three entries:
      "english"  — words from the real English corpus (requires --english-path)
      "random"   — random EVA grapheme sequences (structureless null)

    The English source is loaded fresh from disk each call so the script
    can be run on different machines without code changes.
    """
    sources = {}

    # English source
    if english_path:
        sources["english"] = load_english_corpus(english_path)
        print(f"    Loaded {len(sources['english'])} English words from {english_path}")
    else:
        # Fallback: tile the hardcoded Moby Dick snippet to ~5000 words
        en_sents, _ = ev.make_english_control()
        en_words = [w.lower() for sent in en_sents for w in sent]
        sources["english"] = (en_words * (5000 // len(en_words) + 1))[:5000]
        print(f"    English fallback: tiled Moby Dick snippet to {len(sources['english'])} words")
        print(f"    NOTE: use --english-path data/english.txt for full results")

    # Random EVA source (null baseline)
    rng = random.Random(999)
    sources["random"] = [
        "".join(rng.choice(ALL_GRAPHEMES) for _ in range(rng.randint(2, 6)))
        for _ in range(max(37000, len(sources["english"])))
    ]

    return sources


def grille_sequential_configs(n_words: int = 37000,
                               source_words: Optional[Dict[str, List[str]]] = None
                               ) -> List[GrilleConfig]:
    """
    Row-sequential grille configs — the honest, non-circular MI test.

    Scientific question:
      Can a Cardan grille transmit real sequential structure (from English)
      through to its output, generating MI comparable to the VMS?
      And does it simultaneously achieve Zipfian shape and E->S?

    Three source x four p_jump combinations:

      G_seq_E0   English, p_jump=0.00  (pure sequential — max MI ceiling)
      G_seq_E1   English, p_jump=0.05  (occasional jump — realistic scribe)
      G_seq_E2   English, p_jump=0.10
      G_seq_E3   English, p_jump=0.30
      G_seq_R0   Random,  p_jump=0.00  (null: sequential traversal, no source MI)
      G_seq_R1   Random,  p_jump=1.00  (Rugg baseline: random, no source MI)

    Expected results:
      G_seq_E0: highest MI (bounded by English MI ≈ 0.64); E->S low (English ~31%)
      G_seq_E* : MI decreases monotonically with p_jump
      G_seq_R* : MI ≈ 0 regardless of p_jump (no source structure to transmit)

    Interpretation:
      If no config hits MI ≈ 0.230 with E->S ≈ 80% simultaneously:
        The grille faces the same Sig3/Sig4 tension as the slot generator.
      If a config hits both:
        Rugg's model can explain the VMS joint profile.

    Note on circularity:
      English source: NOT circular (independent of our generator)
      Random source:  NOT circular (structureless null)
      VMS Markov source: CIRCULAR (explicitly excluded from this config set)
    """
    if source_words is None:
        source_words = {}

    eng_words  = source_words.get("english", [])
    rand_words = source_words.get("random", [])

    configs = []
    if eng_words:
        for p_jump, label_suffix in [
            (0.00, "p=0.00 [pure sequential]"),
            (0.05, "p=0.05 [realistic jump]"),
            (0.10, "p=0.10"),
            (0.30, "p=0.30"),
        ]:
            configs.append(GrilleConfig(
                f"G_seq English {label_suffix}",
                mode="SEQUENTIAL", n_holes=4, n_words=n_words,
                p_jump=p_jump, source_words=eng_words))

    if rand_words:
        for p_jump, label_suffix in [
            (0.00, "p=0.00 [sequential null]"),
            (1.00, "p=1.00 [random null]"),
        ]:
            configs.append(GrilleConfig(
                f"G_seq Random  {label_suffix}",
                mode="SEQUENTIAL", n_holes=4, n_words=n_words,
                p_jump=p_jump, source_words=rand_words))

    return configs


